public class BandCard {

    private String bandcardurl;

    private String bandcardname;

    private String bandcardnumber;

    public String getBandcardurl() {
        return bandcardurl;
    }

    public void setBandcardurl(String bandcardurl) {
        this.bandcardurl = bandcardurl;
    }

    public String getBandcardname() {
        return bandcardname;
    }

    public void setBandcardname(String bandcardname) {
        this.bandcardname = bandcardname;
    }

    public String getBandcardnumber() {
        return bandcardnumber;
    }

    public void setBandcardnumber(String bandcardnumber) {
        this.bandcardnumber = bandcardnumber;
    }
}
